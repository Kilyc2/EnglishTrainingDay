package com.android.tfc.englishtrainingday.entities;

public class SectionTabObj {

    private int idSectionTab;
    private String level;
    private int idSection;
    private String nameTab;
    private String textTab;

    public SectionTabObj() {
    }

    public int getIdSectionTab() {
        return idSectionTab;
    }

    public String getLevel() {
        return level;
    }

    public int getIdSection() {
        return idSection;
    }

    public String getNameTab() {
        return nameTab;
    }

    public String getTextTab() {
        return textTab;
    }
}
